export const checkInputNumber = (data?: string) => {
  if (!data) return false;
  const regex = /^[0-9]*\.?[0-9]*$/;
  return !!data.match(regex);
};

export const isPhone = (phone: string) => {
  const regex = /(84|0[3|5|7|8|9])+([0-9]{8})\b/;
  if (!phone) return false;
  if (!regex.test(phone)) return false;
  return true;
};

export const isAppleDevice = () => {
  const regex = /Mac|iPod|iPad|iPhone/;
  return regex.test(navigator.platform);
};

export const isValidSKU = (v: string) => {
  if (!v) return false;
  const regex = /^[^a-f][0-9]+$/;
  return regex.test(v);
};
